package Ders10_StringManipulations3;

public class C08_ForLoop {
    public static void main(String[] args) {

        // 10 kere "Hello World " yazdiralim
        // ama her yazisinda 1'den 10'a kadar sira ile yazdiralim

        for (int i = 1; i <=10 ; i++) {
            System.out.println(i + " - Hello World");
        }
    }
}
